// Get attractions by theme
export function getAttractionsByTheme(theme: AttractionTheme): Attraction[] {
  return allAttractions.filter((a) => a.themes.includes(theme));
}

// Get attractions by park
export function getAttractionsByPark(park: string): Attraction[] {
  return allAttractions.filter((a) => a.park === park);
}

// Get unique parks
export function getParks(): string[] {
  return [...new Set(allAttractions.map((a) => a.park))];
}
